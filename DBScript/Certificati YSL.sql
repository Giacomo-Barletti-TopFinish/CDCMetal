

CREATE TABLE CDC_VERNICICOPRENTI
(
    IDVERNICICOPRENTI NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    IDDETTAGLIO NUMBER NOT NULL,
    UTENTE VARCHAR2(50) NOT NULL,
    DATAINSERIMENTO DATE NOT NULL,
    DATATEST DATE NOT NULL,
    NUMEROCAMPIONI NUMBER(3) NOT NULL,
    FORNITORE VARCHAR2(25) NOT NULL,
    TURBULA VARCHAR2(1) NOT NULL,
    QUADRETTATURA VARCHAR2(1) NOT NULL,
     CONSTRAINT PK_IDVERNICICOPRENTI PRIMARY KEY (IDVERNICICOPRENTI),
    FOREIGN KEY (IDDETTAGLIO)
    REFERENCES CDC_DETTAGLIO (IDDETTAGLIO) ENABLE
);

CREATE INDEX IDX_CDC_VERNICICOPRENTI ON CDC_VERNICICOPRENTI(IDDETTAGLIO);



CREATE TABLE CDC_TENUTACIDONITRICO
(
    IDTENUTA NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    IDDETTAGLIO NUMBER NOT NULL,
    UTENTE VARCHAR2(50) NOT NULL,
    DATAINSERIMENTO DATE NOT NULL,
    DATATEST DATE NOT NULL,
    NUMEROCAMPIONI NUMBER(3) NOT NULL,
    BOLLA VARCHAR2(25) NULL,
    DATADDT DATE NULL,
    ESITO VARCHAR2(1) NOT NULL,
     CONSTRAINT PK_IDTENUTA PRIMARY KEY (IDTENUTA),
    FOREIGN KEY (IDDETTAGLIO)
    REFERENCES CDC_DETTAGLIO (IDDETTAGLIO) ENABLE
);

CREATE INDEX IDX_CDC_TENUTACIDONITRICO ON CDC_TENUTACIDONITRICO(IDDETTAGLIO);