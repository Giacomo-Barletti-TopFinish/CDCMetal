

CREATE TABLE CDC_CERTIFICATIPIOMBO
(
    IDCERTIFICATIPIOMBO NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ELEMENTO VARCHAR2(30) NOT NULL,
    CODICE VARCHAR2(40) NOT NULL,
    MATERIALE VARCHAR2(20) NOT NULL,
    LOTTO VARCHAR2(20) NOT NULL,
    LUNGHEZZA FLOAT NOT NULL,
    LARGHEZZA FLOAT NOT NULL,
    SPESSORE FLOAT NULL,
    METODO VARCHAR2(20) NOT NULL,
    PESOCAMPIONE FLOAT NOT NULL,
    MATRACCIOLO FLOAT NOT NULL,
    CONCENTRAZIONE FLOAT NOT NULL,
    PBPPM FLOAT NOT NULL,
    CDPPM FLOAT NOT NULL,
    ESITO VARCHAR(5) NOT NULL,
    DATACERTIFICATO DATE NOT NULL,
    UTENTE VARCHAR2(50) NOT NULL,
    DATAINSERIMENTO DATE NOT NULL,
     CONSTRAINT PK_IDCERTIFICATIPIOMBO PRIMARY KEY (IDCERTIFICATIPIOMBO)
);

CREATE TABLE CDC_ASSOCIAZIONEPIOMBO
(
    IDASSOCIAZIONEPIOMBO NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
IDCERTIFICATIPIOMBO NUMBER NOT NULL,
IDDETTAGLIO NUMBER NOT NULL,
PESOARTICOLO FLOAT NOT NULL,
PESOMATERIAPRIMA FLOAT NOT NULL,
PESOPRODUZIONE FLOAT NOT NULL,
    UTENTE VARCHAR2(50) NOT NULL,
    DATAINSERIMENTO DATE NOT NULL,
     CONSTRAINT PK_IDASSOCIAZIONEPIOMBO PRIMARY KEY (IDASSOCIAZIONEPIOMBO)
);

CREATE INDEX IDX_CDC_ASSOCIAZIONEPIOMBO_1 ON CDC_ASSOCIAZIONEPIOMBO(IDDETTAGLIO);
